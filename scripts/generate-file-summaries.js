#!/usr/bin/env node

/**
 * Generate File Summaries — Produces per-file analysis prompts for AI processing.
 *
 * Usage: node generate-file-summaries.js /path/to/project [--extensions .ts,.tsx,.js,.jsx]
 *
 * This script does NOT call an AI API directly. Instead, it generates markdown files
 * containing the source code wrapped in the analysis prompt template from Prompt 03.
 * You then feed these to your AI tool of choice.
 *
 * Output: Creates a `refactor-notes/file-analysis/` directory with one markdown file
 * per source file, ready to be processed by an AI.
 */

const fs = require("fs");
const path = require("path");

const IGNORE_DIRS = new Set([
  "node_modules", ".git", "dist", "build", "coverage", ".next", ".nuxt",
  "__pycache__", ".venv", "venv", "target", "vendor", ".cache", ".turbo",
]);

const DEFAULT_EXTENSIONS = new Set([
  ".ts", ".tsx", ".js", ".jsx", ".mjs", ".cjs",
  ".py", ".rs", ".go", ".rb", ".java", ".kt",
  ".vue", ".svelte",
]);

const MAX_FILE_SIZE = 50000; // Skip files larger than 50KB

function main() {
  const args = process.argv.slice(2);
  const projectPath = args[0];

  if (!projectPath) {
    console.error("Usage: node generate-file-summaries.js /path/to/project [--extensions .ts,.tsx]");
    process.exit(1);
  }

  const resolvedPath = path.resolve(projectPath);

  if (!fs.existsSync(resolvedPath)) {
    console.error(`Error: Directory not found: ${resolvedPath}`);
    process.exit(1);
  }

  // Parse extensions flag
  let extensions = DEFAULT_EXTENSIONS;
  const extIdx = args.indexOf("--extensions");
  if (extIdx !== -1 && args[extIdx + 1]) {
    extensions = new Set(args[extIdx + 1].split(",").map((e) => e.trim()));
  }

  // Create output directory
  const outputDir = path.join(resolvedPath, "refactor-notes", "file-analysis");
  fs.mkdirSync(outputDir, { recursive: true });

  // Find source files
  const files = findSourceFiles(resolvedPath, extensions);
  console.log(`Found ${files.length} source files to analyze.\n`);

  // Generate analysis prompts
  let generated = 0;
  let skipped = 0;

  for (const file of files) {
    const relativePath = path.relative(resolvedPath, file);
    const stat = fs.statSync(file);

    if (stat.size > MAX_FILE_SIZE) {
      console.log(`⏭  Skipping (too large): ${relativePath} (${(stat.size / 1024).toFixed(1)}KB)`);
      skipped++;
      continue;
    }

    const content = fs.readFileSync(file, "utf-8");
    const prompt = generateAnalysisPrompt(relativePath, content);

    // Create output file
    const safeName = relativePath.replace(/[/\\]/g, "__").replace(/\./g, "_");
    const outputPath = path.join(outputDir, `${safeName}.md`);
    fs.writeFileSync(outputPath, prompt);

    console.log(`✅ Generated: ${relativePath}`);
    generated++;
  }

  console.log(`\nDone. Generated ${generated} analysis prompts, skipped ${skipped}.`);
  console.log(`Output directory: ${outputDir}`);
  console.log(`\nNext step: Feed these files to your AI tool for analysis.`);
  console.log(`You can process them individually or concatenate them for batch analysis.`);
}

function findSourceFiles(dir, extensions) {
  const files = [];

  function walk(currentDir) {
    let entries;
    try {
      entries = fs.readdirSync(currentDir, { withFileTypes: true });
    } catch {
      return;
    }

    for (const entry of entries) {
      if (IGNORE_DIRS.has(entry.name)) continue;
      if (entry.name.startsWith(".") && entry.isDirectory()) continue;

      const fullPath = path.join(currentDir, entry.name);

      if (entry.isDirectory()) {
        walk(fullPath);
      } else if (entry.isFile()) {
        const ext = path.extname(entry.name);
        if (extensions.has(ext)) {
          // Skip test files — they'll be analyzed separately
          if (
            !entry.name.includes(".test.") &&
            !entry.name.includes(".spec.") &&
            !fullPath.includes("__tests__")
          ) {
            files.push(fullPath);
          }
        }
      }
    }
  }

  walk(dir);
  return files.sort();
}

function generateAnalysisPrompt(relativePath, content) {
  const lineCount = content.split("\n").length;

  return `# File Analysis: ${relativePath}

**Lines:** ${lineCount}
**Generated by:** Refactor Pilot — generate-file-summaries.js

---

Analyze this source file and produce a structured summary:

1. **Purpose** (one sentence)
   What does this file do? What is its primary responsibility?

2. **Exports**
   List everything this file exports (functions, classes, components, constants, types).
   For each export, describe what it does in one line.

3. **Imports & Dependencies**
   - Internal imports (other project files)
   - External imports (npm packages, pip packages, etc.)
   - Note any circular dependency risks

4. **Complexity Indicators**
   - Line count: ${lineCount}
   - Number of functions/methods
   - Nesting depth (deepest level)
   - Any functions over 50 lines

5. **Code Smells**
   Identify if present: duplicated logic, god functions, long parameter lists,
   magic numbers, mixed responsibilities, global mutation, commented-out code,
   TODO/FIXME notes, excessive \`any\` types, \`!important\` in CSS.

6. **Data Flow**
   - Where does this file get its data?
   - Where does it send data?
   - Any side effects?

7. **Refactoring Opportunities**
   Suggest 1-3 specific refactoring actions.

---

## Source Code

\`\`\`
${content}
\`\`\`
`;
}

main();
